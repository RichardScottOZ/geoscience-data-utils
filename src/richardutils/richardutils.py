import math


def richardfunction(n: float) -> float:
    """
    My function makes square roots.

    Args:
        n: A number of some kind.

    Returns:
        The squarest root.

    Examples:
    >>> richardfunction(9)
    3.0
    >>> richardfunction(100)
    10.0
    """
    return math.sqrt(n)
