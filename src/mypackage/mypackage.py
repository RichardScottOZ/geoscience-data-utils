import math


def myfunction(n: float) -> float:
    """
    My function makes square roots.

    Args:
        n: A number of some kind.

    Returns:
        The squarest root.

    Examples:
    >>> myfunction(9)
    3.0
    >>> myfunction(100)
    10.0
    """
    return math.sqrt(n)
